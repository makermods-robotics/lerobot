def _nearest(src, dst, x):
    if x <= src[0]:
        return dst[0]
    if x >= src[-1]:
        return dst[-1]
    for i in range(1, len(src)):
        if x < src[i]:
            return dst[i - 1] if abs(x - src[i - 1]) < abs(src[i] - x) else dst[i]
    return dst[-1]

def stroke_mm_to_rad(mm: float) -> float:
    return _nearest(_DIST, _ANG, mm)

def rad_to_stroke_mm(rad: float) -> float:
    return _nearest(_ANG, _DIST, rad)

def stroke_mm_to_norm(mm: float) -> float:
    return mm / GRIPPER_MAX_MM * 100.0

def norm_to_stroke_mm(norm: float) -> float:
    return norm / 100.0 * GRIPPER_MAX_MM
